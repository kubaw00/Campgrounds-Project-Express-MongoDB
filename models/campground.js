import mongoose from 'mongoose';
const Schema = mongoose.Schema;

const CampgorundSchema = new Schema({
  title: String,
  image: String,
  price: Number,
  description: String,
  location: String,
  author: {
    type: Schema.Types.ObjectId,
    ref: 'User',
  },
  reviews: [
    {
      type: Schema.Types.ObjectId,
      ref: 'Review',
    },
  ],
});

export default mongoose.model('Campground', CampgorundSchema);
